#define LOCAL_TIMER_LOAD		(PBASE+0x0000B400)
#define LOCAL_TIMER_VALUE		(PBASE+0x0000B404)
#define LOCAL_TIMER_CTRL		(PBASE+0x0000B408)
#define LOCAL_TIMER_IRQ_CLEAR	(PBASE+0x0000B40C)
#define LOCAL_TIMER_RAW_IRQ		(PBASE+0x0000B410)
#define LOCAL_TIMER_MASKED_IRQ	(PBASE+0x0000B414)
#define LOCAL_TIMER_RELOAD		(PBASE+0x0000B418)

#define LOCAL_TIMER_CTRL_23bit			(1<<1)
#define LOCAL_TIMER_CTRL_PRESCALE256	(2<<2)
#define LOCAL_TIMER_CTRL_INT_ENABLE		(1<<5)
#define LOCAL_TIMER_CTRL_ENABLE			(1<<7)

#endif
